#include "stm32f10x.h"
#include "system.h"

/********************************************************************
函数功能：将传入的nCount初始数值递减1，直到0返回，实现软件延时。
入口参数：uint32_t nCount：初始数值。
返    回：无。
备    注：无。
********************************************************************/
void Delay(uint32_t nCount)
{
  for(; nCount != 0; nCount--);
}
