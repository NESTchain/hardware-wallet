void NVIC_Config(void)
{
	//NVIC_InitTypeDef NVIC_InitStructure; 
	/* Configure the NVIC Preemption Priority Bits */  
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_4);
	//配置为16个先占优先级（即主优先级），0个从优先级模式
}
